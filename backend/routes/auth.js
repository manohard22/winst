          dateOfBirth: user.date_of_birth,
          gender: user.gender,
          address: user.address,
          city: user.city,
          state: user.state,
          country: user.country,
          pincode: user.pincode,
          collegeName: user.college_name,
          degree: user.degree,
          branch: user.branch,
          yearOfStudy: user.year_of_study,
          cgpa: user.cgpa,
